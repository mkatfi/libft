#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*d;

	d = (char *)s;
	i = 0;
	while (i < n)
	{
		d[i] = 0;
		i++;
	}
}
int main()
{
	int i = 0;
	int b[4] = {1,1,3,2};
	ft_bzero(b,5);
	while (i < 4)
	{
		printf("%d\n",b[i]);
		i++;
	}
	
}
