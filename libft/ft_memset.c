#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*d;

	d = (char *)b;
	i = 0;
	while (len > i)
	{
		d[i] = c;
		i++;
	}
	return (d);
}
